    public void populateTable() {
        Organization organization1 = null;
      
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org.getName().equals("PlowTruck Organization")) {
                organization1 = org;
                break;
            }
        }

        DefaultTableModel model = (DefaultTableModel) tblPlowTruck.getModel();

        model.setRowCount(0);

         for(UserAccount ua : organization1.getUserAccountDirectory().getUserAccountList()){
            Object[] row = new Object[4];
            row[0] = ua;
            row[1] = ua.getEmployee().getName();
           
            row[2] = ua;          
            
           String status = ua.getEmployee().getStatus();
           row[3] = status == null ? "Free" : status;
            model.addRow(row);
         }
            
    }

