    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPlowTruck.getSelectedRow();
        UserAccount ua;

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            ua = (UserAccount) tblPlowTruck.getValueAt(selectedRow, 2);
        }

        ua.getWorkQueue().getWorkRequestList().add(request);
        
        request.setPlowTruckID(ua.getEmployee().getId());
        request.setPlowTruckName(ua.getEmployee().getName());
        ua.getEmployee().setStatus("Assigned");
        request.setStatus("Processing");
        request.setReceiver(ua);

        txtStreetName.setText(request.getStreet().getName());
        txtSensorArea.setText(request.getSensor().getArea());
        txtTruckId.setText(String.valueOf(request.getPlowTruckID()));
        
    }//GEN-LAST:event_btnAssignActionPerformed

