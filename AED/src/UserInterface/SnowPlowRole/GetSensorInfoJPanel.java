    public void populateStreetTable() {
        DefaultTableModel model = (DefaultTableModel) tbleStreet.getModel();

        model.setRowCount(0);

        for (Street street : organization.getStreetDirectory().getStreetList()) {
            Object[] row = new Object[3];
            row[0] = street.getId();
            row[1] = street;
            //  row[2] = street;
            model.addRow(row);
        }
    }

    public void populateSensorTable() {
        DefaultTableModel model = (DefaultTableModel) tblSensor.getModel();

        model.setRowCount(0);

        for (Street street : organization.getStreetDirectory().getStreetList()) {
            for (Sensor sensor : street.getSensorDirectory().getSensorList()) {
                Object[] row = new Object[3];
                row[0] = sensor.getId();
                row[1] = sensor.getArea();
                row[2] = street.getName();
                model.addRow(row);
            }

        }
    }

