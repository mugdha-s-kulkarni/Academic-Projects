    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbleStreet.getSelectedRow();
        Street selectedStreet;

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            selectedStreet = (Street) tbleStreet.getValueAt(selectedRow, 1);
        }

        String area = txtArea.getText();
        int count = 0;

        try {
            for (Street s : organization.getStreetDirectory().getStreetList()) {
                for (Sensor se : s.getSensorDirectory().getSensorList()) {
                    if (se.getArea().equalsIgnoreCase(area)) {
                        count++;
                    }
                }
            }
            if (txtArea.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Fields can't be emptys", "Error", JOptionPane.ERROR_MESSAGE);

            } else if (count == 0) {
                Sensor sensor = new Sensor();
                sensor.setArea(area);
                String a = sensor.getArea();

                selectedStreet.getSensorDirectory().getSensorList().add(sensor);

                JOptionPane.showMessageDialog(null, "Sensor added successfully!!!");

                populateSensorTable();
                txtArea.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Duplicate name for sensor area not allowed", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
        }


    }//GEN-LAST:event_btnAddActionPerformed

