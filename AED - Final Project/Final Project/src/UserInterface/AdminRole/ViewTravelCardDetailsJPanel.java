    public void populateUserTable() {
        
        try {
            tblUser.removeAll();
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);
        Organization org = null;

        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof UserOrganization) {
                org = o;
                break;
            }
        }

        for (Employee e : org.getEmployeeDirectory().getEmployeeList()) {
            Object[] row = new Object[3];
            row[0] = e.getName();
            row[1] = ((User) e).getNumberOfCards();
            row[2] = ((User) e).getOrderTotal();
            model.addRow(row);
        } 
        } catch (Exception e) {
        }
       
    }

    public int populateRevenue() {
     

        Organization org = null;
        int total = 0;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof UserOrganization) {
                org = o;
                break;
            }
        }
        try {
            for (Employee e : org.getEmployeeDirectory().getEmployeeList()) {
                total = total + ((User) e).getOrderTotal();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Values must not be entered till now", "Warning", JOptionPane.WARNING_MESSAGE);
        }

        return total;
    }

    private String getBestCustomer() {

        int largest = Integer.MIN_VALUE;
        String name = " ";
        Organization org = null;

        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof UserOrganization) {
                org = o;
                break;
            }
        }

        for (Employee e : org.getEmployeeDirectory().getEmployeeList()) {

            if (((User) e).getOrderTotal() > largest) {
                largest = ((User) e).getOrderTotal();
                name = e.getName();
            }
        }
        return name;
    }

