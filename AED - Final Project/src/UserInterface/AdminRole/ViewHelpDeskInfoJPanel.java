    public void populateHelpTable(){
        
        try {
             tblHelpDesk.removeAll();
        DefaultTableModel model = (DefaultTableModel) tblHelpDesk.getModel();
        model.setRowCount(0);
        Organization org = null;
        
        for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
          if(o instanceof HelpDeskOrganization){
              org = o;
              break;
          }
        }
        
        for(UserAccount e : org.getUserAccountDirectory().getUserAccountList()){
            Object[] row = new Object[4];
            row[0] = e.getEmployee().getName();
            row[1] = e.getWorkQueue().getWorkRequestList().size();
            row[2] = e.getNumberOfResolvedIssues();
            row[3] = e.getNumberOfUnResolvedIssues();
            model.addRow(row);
         }
            
        } catch (Exception e) {
        }
       
     }
    
    private void getBusyEmployee() {
        Organization org = null;
        int count = 0;
        try {
            for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (o instanceof HelpDeskOrganization) {
                    org = o;
                    break;
                }
            }

            for(UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {

                if (ua.getEmployee().getStatus().equalsIgnoreCase("Busy")) {
                    count++;
                }
            }

            txtBusyEmployee.setText(String.valueOf(count));
            int size = org.getEmployeeDirectory().getEmployeeList().size();
            txtFreeEmployee.setText(String.valueOf(size - count));

           

        } catch (Exception e) {

        }
    }
     private String getBestEmployee() {
         
              float largest = Integer.MIN_VALUE;
        String name = "";
         Organization org = null;
        
        for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
          if(o instanceof HelpDeskOrganization){
              org = o;
              break;
          }
        }
        try{
        for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
            int size = ua.getWorkQueue().getWorkRequestList().size();
            int solved = ua.getNumberOfResolvedIssues();
            
            int fraction = (solved/size)*10;
                if ( fraction> largest) {
                    largest = fraction;
                    name = ua.getEmployee().getName();
                }            
        }
        }catch(Exception e){
        }
         return name;     
    }
